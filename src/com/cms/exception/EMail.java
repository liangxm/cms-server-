package com.cms.exception;

/**
 * email pro class
 * 
 * @author lxm
 * @time 2013-1-5 12:12:53
 */
public class EMail {
	/** smtp server*/
	private String hostname;
	
	/** sender's email address*/
	private String from;
	
	/** accepter's email address*/
	private String to;
	
	/** email's subject*/
	private String subject;
	
	/** email's content*/
	private String text;
	
	/** attachment url*/
	private String attachment;
	
	/** sender's email account username and password*/
	private String username;
	private String password;

	public EMail(String hostname, String from, String to, String subject,
			String text, String username, String password) {
		super();
		this.hostname = hostname;
		this.from = from;
		this.to = to;
		this.subject = subject;
		this.text = text;
		this.username = username;
		this.password = password;
	}

	public EMail(String hostname, String from, String to, String subject,
			String text, String attachment, String username, String password) {
		super();
		this.hostname = hostname;
		this.from = from;
		this.to = to;
		this.subject = subject;
		this.text = text;
		this.attachment = attachment;
		this.username = username;
		this.password = password;
	}

	public String getHostname() {
		return hostname;
	}

	public void setHostname(String hostname) {
		this.hostname = hostname;
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
	public String getAttachment() {
		return attachment;
	}

	public void setAttachment(String attachment) {
		this.attachment = attachment;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
